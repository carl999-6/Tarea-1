    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed
String operacion = this.lbl_casilla.getText();
String resultado = "";
        this.segundonumero = Float.parseFloat(operacion);
        
switch(this.operador){
    case "+":
        resultado = nocero(this.primernumero + this.segundonumero);
        this.lbl_casilla.setText(resultado);break;
    case "-":
        resultado = nocero(this.primernumero - this.segundonumero);
        this.lbl_casilla.setText(resultado);break;
    case "*":
        resultado = nocero(this.primernumero * this.segundonumero);
        this.lbl_casilla.setText(resultado);break;
    case "/":
        if(this.segundonumero==0){
            resultado = "No se puede dividir entre cero";
        } else {
resultado = nocero(this.primernumero / this.segundonumero);
this.lbl_casilla.setText(resultado);}break;

}
guardarOperacion(this.primernumero + " " + this.operador + " " + this.segundonumero,resultado);
 Toolkit.getDefaultToolkit().beep();
        // TODO add your handling code here:
    }//GEN-LAST:event_btn_igualActionPerformed

    
    public String nocero(float resultado){
    String retorno="";
    
    retorno = Float.toString(resultado);
    
    if(resultado%1==0){
    retorno = retorno.substring(0, retorno.length()-2);
    }
    
    return retorno;
    
    }
    
    
