    private void guardarOperacion (String operacion, String resultado){
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("bitacoraCalculadora.txt",true))){
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
            String fechaHora = LocalDateTime.now().format(formatter);
            
            writer.write(fechaHora + "  -----  " + operacion + " = resultado: " + resultado);
            writer.newLine();
            
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
